# OPERADORES ARITMÉTICOS
a = 10
b = 6

# Suma "+"

print("Suma de a mas b = ", a + b)

# Resta "-"

print("Resta de a menos b = ", a - b)

# Multiplicación "*"

print("Multiplicación de a por b = ", a * b)

# División Decimal "/"

print("División de a entre b = ", a / b)

# División entera "//"

print("División entera de a entre b = ", a // b)

# Exponente "**"

print("Exponente de a elevado a b = ", a ** b)

# Módulo "%"

print("Resto de la división de a entre b = ", a % b)

# OPERADORES DE COMPARACIÓN

# Mayor que ">"

print("A es mayor que B? ", a > b)

# Menor que "<"

print("A es menor que B? ", a < b)

# Mayor o igual que ">="

print("A es mayor o igual que B? ", a >= b)

# Menor o igual que "<="

print("A es menor o igual que B? ", a <= b)

# Igual que "=="

print("A es igual que B? ", a == b)

# Distinto de "!="

print("A es distinto de B? ", a != b)